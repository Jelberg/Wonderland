    public boolean camposVacios() {
        String numTarj=tarjetatxt.getText();
        String code=codigotxt.getText();
        if (Validar.validarVacio(tarjetatxt, "Debe indicar numero de tarjeta") == 1
                && Validar.validarEntero(numTarj, "Numero de Tarjeta Invalido") == 1
                && Validar.validarVacio(marcatxt, "Debe indicar la Marca Comercial") == 1
                && Validar.validarVacio(codigotxt, "Debe indicar numero de tarjeta") == 1
                && Validar.validarEntero(code, "Codigo de Tarjeta invalido") == 1
                && Validar.validarFecha(fecha, "Debe incicar la Fecha de vencimiento") == 1) {
            return false;
        } else {
            return true;
        }
    }

