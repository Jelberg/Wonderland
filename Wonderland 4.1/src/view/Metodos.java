    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed
        // TODO add your handling code here:
        if (!camposVacios()) {
            String fechaven = df.format(fecha.getDate());
            ControladorMetodos.agregarMetodo(usuario.getSeudonimo(), tarjetatxt.getText(), marcatxt.getText(), codigotxt.getText(), fechaven, DatCli);
            MetodosDePago control2 = new MetodosDePago(usuario);
            control2.setLocationRelativeTo(null);
            control2.setVisible(true);
            this.dispose();
            JOptionPane.showMessageDialog(null, "Metodo de págo agregado", "Notificacion", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BotonAgregarActionPerformed

