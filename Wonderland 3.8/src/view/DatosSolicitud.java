    private void LabelAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LabelAceptarMouseClicked
        // TODO add your handling code here:
        Usuario solicitud = DatRol.buscarPersona(usuarioCambio.getSeudonimo());
        Usuario cambio = DatUsu.buscarPersona(usuarioCambio.getSeudonimo());
        solicitud.setNombre("Si");
        cambio.setRol(Cambio.getRol());
        DatUsu.actualizarUsuario(cambio);
        DatRol.actualizarUsuarioCambio(solicitud);
        JOptionPane.showMessageDialog(null, "Se mando la respuesta a la Solicitud", "Notificacion", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_LabelAceptarMouseClicked

