    public ListaBusquedainventario(String po) {
        initComponents();
        ArrayList<Productos> Lista = regCat.todaslasCategorias();
        for (Productos pro : Lista) {
            if (pro.getCategoria().equals(po) != false) {
                Productos produc = regPro.buscarProductoSerial(pro.getSerial());
                modelo.addElement(produc);
                listaCategoriasBusqueda.setModel(modelo);
            }
        }
    }

