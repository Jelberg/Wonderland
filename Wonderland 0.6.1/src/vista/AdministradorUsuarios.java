    private void EliminarUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarUsuActionPerformed
           if    (Validar.validarVacio(nombreUsuario, "Debe indicar nombre de la editorial a eliminar que desea eliminar")==1)
           {   int valor = 1;
                if (valor == 1) {
                   Usuario est = DatUsu.buscarPersona(nombreUsuario.getText());
                    if (est == null) {
                        JOptionPane.showMessageDialog(null, "Usuario no Existe", "Error proveedor  no encontrado", JOptionPane.ERROR_MESSAGE);
                    }
                    else {
                       
                       email.setText(String.valueOf(est.getCorreo()));
                       int confirmacion = JOptionPane.showOptionDialog(null, "¿Desea Realizar La Operación?", "::::...Confirmación...:::",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
                       if (confirmacion == 0) {
                           
                           DatUsu.borrarPersona(nombreUsuario.getText());
                       }
                }
            }
           }
    }//GEN-LAST:event_EliminarUsuActionPerformed

